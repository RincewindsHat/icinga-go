#!/bin/sh

types="Host Service User HostGroup"

head="// Code generated by $0 $@; DO NOT EDIT.

package icinga

import (
	\"fmt\"
	\"net/url\"
)
"

args=`getopt o: $*`
if test $? -ne 0
then
	echo "usage: lookupgen.sh [-o file]"
	exit 2
fi
set -- $args
while test "$#" -ne 0
do
	case "$1"
	in
		-o)
			file="$2"; shift; shift;;
		--)
			shift; break;;
	esac
done

if test -n "$file"
then
	echo "$head" > "$file"
else
	echo "$head"
fi

for t in $types
do
	if test -n "$file"
	then
		awk -v "type=$t" -f crud.awk crud.skel | gofmt >> "$file"
	else
		awk -v "type=$t" -f crud.awk crud.skel | gofmt
	fi
done
